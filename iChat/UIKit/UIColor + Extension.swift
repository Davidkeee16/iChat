import Foundation
import UIKit


extension UIColor {
    
    static func redButton() -> UIColor {
        return #colorLiteral(red: 0.8924437165, green: 0, blue: 0, alpha: 1)
    }
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.9999999404, green: 1, blue: 1, alpha: 1)
    }
    static func darkButton() -> UIColor {
        return #colorLiteral(red: 0.2000000477, green: 0.2000000477, blue: 0.2000000477, alpha: 1)
    }
    static func textFieldLineColor() -> UIColor {
        return #colorLiteral(red: 0.9058824182, green: 0.9058824182, blue: 0.9058824182, alpha: 1)
    }
    static func addImageButtonColor() -> UIColor {
        return #colorLiteral(red: 0.8470587134, green: 0.8470587134, blue: 0.8470587134, alpha: 1)
    }
}

    
    
